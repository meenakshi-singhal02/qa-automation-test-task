package pageObjects.WebServiceDTOs;

public class WebServiceDTO {

	private int postId;
	private int id;
	private String name;
	private String email;
	private String body;

	public int getPostId() {
		return this.postId;
	}

	public int getId() {
		return this.id;
	}

	public String getName() {
		return this.name;
	}

	public String getEmail() {
		return this.email;
	}

	public String getBody() {
		return this.body;
	}

}
